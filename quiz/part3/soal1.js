//LOOPING PERTAMA HITUNG MAJU
let hitungMaju = 7;
while (hitungMaju < 10){
    console.log('Maju -' + hitungMaju);
    hitungMaju++;
}


//LOOPING PERTAMA HITUNG MUNDUR
let hitungMundur = 6;
while (hitungMundur > 1){
    console.log('Mundur -'+ hitungMundur);
    hitungMundur--;
}